USE uuid;

DELIMITER //

CREATE FUNCTION binuuid()
RETURNS BINARY(16)
NO SQL
BEGIN
  RETURN UNHEX(REPLACE(UUID(), '-', ''));
END; //

CREATE FUNCTION obinuuid()
RETURNS BINARY(16)
NO SQL
BEGIN
  DECLARE uuid CHAR(36);
  SET uuid = UUID();
  RETURN UNHEX(CONCAT(SUBSTR(uuid, 15, 4),SUBSTR(uuid, 10, 4),SUBSTR(uuid, 1, 8),SUBSTR(uuid, 20, 4),SUBSTR(uuid, 25)));
END; //

DELIMITER ;

CREATE TABLE id (
  uuid INT(11) UNSIGNED AUTO_INCREMENT PRIMARY KEY
) ENGINE=INNODB;

CREATE TABLE uuid (
  uuid CHAR(36) PRIMARY KEY
) ENGINE=INNODB;

CREATE TABLE binuuid (
  uuid BINARY(16) PRIMARY KEY
) ENGINE=INNODB;

CREATE TABLE obinuuid (
  uuid BINARY(16) PRIMARY KEY
) ENGINE=INNODB;